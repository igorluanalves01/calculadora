# Onde definiremos as funções e as implementaremos
def soma(valorx, valora):
    print('chamando função de somar')
    return valorx + valora


def subtrair(valorv, valorh):
    print('chamando função de substrair')
    # print(50 - 20)
    return valorv - valorh

def multiplicacao(valory, valorx):
    print( ' chamando função multiplicacão)')
    return valory * valorx

def divisao(valorm, valorw):
    print(' chamando função divisao')
    if valorw > 0:
        return valorm / valorw
    return 'nao e possivel dividir por 0'

